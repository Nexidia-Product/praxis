-- =============================================================================
-- settings (singleton)
-- =============================================================================
--
-- One row, id='singleton'. The CHECK constraint prevents accidental
-- additional inserts. The application reads with `.eq('id', 'singleton')`
-- and upserts with the same key.

create table public.settings (
  id                          text primary key default 'singleton'
                                check (id = 'singleton'),
  health_score_thresholds     jsonb not null,
  branding                    jsonb not null,
  notification_defaults       jsonb not null,
  custom_field_definitions    jsonb not null default '[]'::jsonb,
  kanban_configs              jsonb not null default '[]'::jsonb,
  role_permissions            jsonb not null,
  enum_extensions             jsonb not null,
  resource_settings           jsonb not null,
  portfolio_quadrants         jsonb not null
);

-- =============================================================================
-- Row-Level Security
-- =============================================================================
--
-- Enable RLS on every public table. No policies = deny-by-default for
-- anon and authenticated roles. The service-role key bypasses RLS, so
-- server-side code continues to work. We'll layer policies on in Stage
-- 2 when the browser starts hitting Supabase directly for auth flows
-- and (eventually) the public /submit idea form.

alter table public.users         enable row level security;
alter table public.projects      enable row level security;
alter table public.tasks         enable row level security;
alter table public.ideas         enable row level security;
alter table public.decisions     enable row level security;
alter table public.notifications enable row level security;
alter table public.templates     enable row level security;
alter table public.audit_log     enable row level security;
alter table public.settings      enable row level security;
